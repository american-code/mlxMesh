func geoForNode(region string, idx int) (float64, float64) {
	coords := regionCoords[region]
	if len(coords) == 0 {
		return 0, 0
	}
	c := coords[idx%len(coords)]
	jitterLat := float64((idx/len(coords))%3-1) * 0.12
	jitterLng := float64((idx/len(coords)+1)%3-1) * 0.12
	return c.lat + jitterLat, c.lng + jitterLng
}

// nodeProfile: regular 32 GB contributor nodes — the common case.
func nodeProfile(region string, idx int) (int, int, string, float64, float64) {
	const memGB = 32

	var baseLat int
	if region == "us" {
		baseLat = 110 + (idx%5)*25
	} else {
		baseLat = 200 + (idx%6)*35
	}

	// All 32 GB nodes can run both models.
	models := "llama-3.2-3b,mixtral-8x7b"
	capPct := 0.65
	tps    := 62.0 + float64((idx%5)*4) // 62–78 tok/s

	return memGB, baseLat, models, capPct, tps
}

// exoProfile: pooled Exo clusters — 128 GB or 256 GB, high throughput.
// idx is 1-based within the exo set for the region.
func exoProfile(region string, idx int) (int, int, string, float64, float64) {
	var memGB int
	var tps float64
	var models string

	if idx%2 == 1 {
		// 256 GB flagship: runs the full model ladder
		memGB  = 256
		tps    = 780.0 + float64((idx%3)*20)
		models = "llama-3.2-3b,mixtral-8x7b,llama-3.1-70b,llama-3.1-405b"
	} else {
		// 128 GB mid-tier
		memGB  = 128
		tps    = 370.0 + float64((idx%3)*15)
		models = "llama-3.2-3b,mixtral-8x7b,llama-3.1-70b"
	}

	var latMS int
	if region == "us" {
		latMS = 80 + (idx%3)*20
	} else {
		latMS = 160 + (idx%3)*30
	}

	capPct := 0.85 // exo clusters commit more capacity
	return memGB, latMS, models, capPct, tps
}

func footer(w *os.File) {
	fmt.Fprint(w, `
networks:
  default:
    name: oim-mesh
`)
}

func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}
