// region writes stub-exo + node service pairs for one region.
// Returns the next available host port.
func region(w *os.File, regionName, coordinatorSvc, coordinatorPort string, count, basePort int) int {
	fmt.Fprintf(w, "\n  # ── %s nodes (%d) %s\n",
		strings.ToUpper(regionName), count, strings.Repeat("─", 54-len(regionName)-len(fmt.Sprint(count))-2))

	port := basePort
	for i := 1; i <= count; i++ {
		nodeSvc := fmt.Sprintf("node-%s-%d", regionName, i)
		exoSvc := fmt.Sprintf("stub-exo-%s-%d", regionName, i)
		nodePort := port
		port++

		memGB, latMS, models, capPct := nodeProfile(regionName, i)

		// stub-exo service
		fmt.Fprintf(w, `
  %s:
    <<: *oim
    command: ["/usr/local/bin/stub-exo"]
    environment:
      STUB_NODE_NAME: %s
      STUB_MODELS: %s
      STUB_MEMORY_GB: "%d"
      STUB_LATENCY_MS: "%d"
    healthcheck:
      test: ["CMD", "wget", "-qO-", "http://localhost:52415/state"]
      interval: 2s
      timeout: 2s
      retries: 10
      start_period: 3s
`, exoSvc, nodeSvc, models, memGB, latMS)

		// node agent service
		fmt.Fprintf(w, `  %s:
    <<: *oim
    hostname: %s
    command:
      - /usr/local/bin/oim
      - node
      - start
      - --coordinator=http://%s:%s
      - --listen=:%d
      - --exo-url=http://%s:52415
      - --reachability-endpoint=http://%s:%d
      - --region=%s
      - --cap=%.1f
    ports:
      - "%d:%d"
    depends_on:
      %s:
        condition: service_healthy
      %s:
        condition: service_healthy
`, nodeSvc, nodeSvc,
			coordinatorSvc, coordinatorPort,
			nodePort,
			exoSvc,
			nodeSvc, nodePort,
			regionName,
			capPct,
			nodePort, nodePort,
			coordinatorSvc,
			exoSvc)
	}
	return port
}

// nodeProfile returns (memGB, latencyMs, modelList, capPct) for a given node.
// Varies deterministically by region and index to simulate heterogeneous hardware.
func nodeProfile(region string, idx int) (int, int, string, float64) {
	// Memory tiers cycling through nodes: small → medium → large → medium → small …
	memTiers := []int{12, 16, 24, 32, 48, 64, 32, 24, 16}
	memGB := memTiers[idx%len(memTiers)]

	// Latency: US is lower, EU is higher. Add per-node jitter.
	var baseLat int
	if region == "us" {
		baseLat = 120 + (idx%5)*30 // 120–240 ms
	} else {
		baseLat = 220 + (idx%6)*40 // 220–420 ms
	}

	// Model sets: nodes with ≥24 GB run both models; smaller nodes run llama only.
	var models string
	if memGB >= 24 {
		models = "llama-3.2-3b,mixtral-8x7b"
	} else {
		models = "llama-3.2-3b"
	}

	// Cap percentage: larger nodes commit more of their memory.
	var capPct float64
	switch {
	case memGB >= 48:
		capPct = 0.75
	case memGB >= 24:
		capPct = 0.60
	default:
		capPct = 0.50
	}

	return memGB, baseLat, models, capPct
}

func footer(w *os.File) {
	fmt.Fprint(w, `
networks:
  default:
    name: oim-mesh
`)
}
