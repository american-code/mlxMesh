// gen-compose generates a docker-compose.yml for a simulated Open Inference Mesh cluster.
// Each node gets its own stub-exo sidecar so no real GPU or Exo install is needed.
//
// Usage:
//
//	go run ./tools/gen-compose [flags] > docker-compose.yml
//
// Flags:
//
//	--us        number of regular US nodes   (default 22)
//	--eu        number of regular EU nodes   (default 22)
//	--exo-us    number of large exo clusters in US  (default 2)
//	--exo-eu    number of large exo clusters in EU  (default 1)
//	--base-port first host port allocated to node agents (default 8765)
package main

import (
	"flag"
	"fmt"
	"os"
	"strings"
)

func main() {
	usCount    := flag.Int("us",       22, "Number of regular US nodes")
	euCount    := flag.Int("eu",       22, "Number of regular EU nodes")
	exoUS      := flag.Int("exo-us",    2, "Number of large exo clusters in US (128/256 GB)")
	exoEU      := flag.Int("exo-eu",    1, "Number of large exo clusters in EU (128/256 GB)")
	basePort   := flag.Int("base-port", 8765, "First host port for node agents")
	flag.Parse()

	w := os.Stdout

	header(w)
	infrastructure(w)

	port := *basePort
	port = region(w, "us", "coordinator-us", "9000", *usCount, *exoUS, port)
	region(w, "eu", "coordinator-eu", "9001", *euCount, *exoEU, port)

	footer(w)
}

func header(w *os.File) {
	fmt.Fprint(w, `# Auto-generated by tools/gen-compose — do not edit by hand.
# Regenerate: go run ./tools/gen-compose [--us=N] [--eu=N] [--exo-us=N] [--exo-eu=N] > docker-compose.yml

x-oim: &oim
  image: oim-sim
  build: .
  restart: unless-stopped

services:
`)
}

