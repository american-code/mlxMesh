// gen-compose generates a docker-compose.yml for a simulated Open Inference Mesh cluster.
// Each node gets its own stub-exo sidecar so no real GPU or Exo install is needed.
//
// Usage:
//
//	go run ./tools/gen-compose [flags] > docker-compose.yml
//
// Flags:
//
//	--us       number of US nodes (default 13)
//	--eu       number of EU nodes (default 12)
//	--base-port first host port allocated to node agents (default 8765)
package main

import (
	"flag"
	"fmt"
	"os"
	"strings"
)

func main() {
	usCount := flag.Int("us", 13, "Number of US nodes")
	euCount := flag.Int("eu", 12, "Number of EU nodes")
	basePort := flag.Int("base-port", 8765, "First host port for node agents")
	flag.Parse()

	w := os.Stdout

	header(w)
	infrastructure(w)

	port := *basePort
	port = region(w, "us", "coordinator-us", "9000", *usCount, port)
	region(w, "eu", "coordinator-eu", "9001", *euCount, port)

	footer(w)
}

// header writes the compose file preamble and shared anchor.
func header(w *os.File) {
	fmt.Fprint(w, `# Auto-generated by tools/gen-compose — do not edit by hand.
# Regenerate: go run ./tools/gen-compose [--us=N] [--eu=N] > docker-compose.yml

x-oim: &oim
  image: oim-sim
  build: .
  restart: unless-stopped

services:
`)
}

